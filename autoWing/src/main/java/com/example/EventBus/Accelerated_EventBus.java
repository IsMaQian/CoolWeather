package com.example.EventBus;

public class Accelerated_EventBus {
	double m_aX;
	double m_aY;
	double m_aZ;
	
	public Accelerated_EventBus(double m_aX,double m_aY,double m_aZ) {
		this.m_aX = m_aX;
		this.m_aY = m_aY;
		this.m_aZ = m_aZ;
	}
	public double geX(){
		return m_aX;
	}
	
	public double geY(){
		return m_aX;
	}
	
	public double geZ(){
		return m_aX;
	}
}
